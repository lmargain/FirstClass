
public class FirstClass
{
    // instance variables - replace the example below with your own
    private int a;
    private int b;

    public FirstClass()
    {
        // initialise instance variables
        a = 2;
        b = 5;
    }

    
    public int addEmUp()
    {
        return a + b;
    }

    public int difference () {
        return a - b;
    }

}
